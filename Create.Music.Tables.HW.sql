CREATE TABLE Artist
(
	Id int not null Primary Key Identity(1,1),
	Name varchar (30) not null,
	Genre varchar(30) not null,
	Number_of_Albums int not null,
	Years_Experience int not null,
	Active bit not null
)

CREATE TABLE Album
(
	Album_Name varchar(30) Primary Key,
	Sales int,
	Release_Year int,
	Artist varchar(30),
	Artist_Id int not null foreign key references Artist(Id)
)
CREATE TABLE Song
(
	Song_Name varchar(30) Primary Key not null,
	Album varchar(30) foreign key references Album(Album_Name),
	Artist varchar(30) not null,
	Record_Label varchar(50),

)

